def dia_semana(numero):
    if numero == 1:
        print("Lunes")
    elif numero == 2:
        print("Martes")
    elif numero == 3:
        print("Miercoles")
    elif numero == 4:
        print("Jueves")
    elif numero == 5:
        print("Viernes")
    elif numero == 6:
        print("Sabado")
    elif numero == 7:
        print("Domingo")
    else:
        print("El numero es incorrecto")
dia_semana(3)
dia_semana(20)

